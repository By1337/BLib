package org.by1337.blib.core.text;

import net.kyori.adventure.text.Component;
import net.kyori.adventure.text.serializer.gson.GsonComponentSerializer;
import org.by1337.blib.minimessage.JsonComponentToANSI;
import org.by1337.blib.text.ComponentToANSI;

public class ComponentToANSIImpl implements ComponentToANSI {
    @Override
    public String convert(Component component) {
        return JsonComponentToANSI.convert(GsonComponentSerializer.gson().serialize(component));
    }

    @Override
    public String buildAndConvert(String legacy) {
        return JsonComponentToANSI.buildAndConvert(legacy);
    }
}
